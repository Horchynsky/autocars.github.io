<p style="font-size: 24pt; color: black;">Should I buy one?</p>
<br>
In the realm of circa-300bhp four-wheel-drive hot hatches, there’s nothing to match the outward character of the A35, and that’s largely down to the materially rich, almost eccentric cabin and some fierce exhaust tuning. If you’re bored of demure Volkswagen Group products, you’ll like it.   
<br><br>
With similar vital statistics, the entry-level AMG is also every bit as heart-poundingly quick from point to point as its rivals from Audi and Volkswagen – perhaps quicker, so accomplished is its body control and engine response. In fact, the A35 is a more accurate, exhilarating device than the S3, and possibly the Golf R, too, even if it can't (or, rather, hasn't been set up to) match the flowing balance of the latter.          
<br><br>
The passive suspension set-up, with too much of a B-road bias, is the only true blot in its copybook, because it limits this AMG's everyday appeal. Sometime in the very near future, we hope to find out first-hand whether adaptive dampers remedy this, but the A35 has enough strength elsewhere to go into upcoming group tests with no small confidence.  
